
public class Main1 {

	public static void main(String[] args) {

		int int1 = 123;		// liczby całkowite, zakres od -2 147 483 648 do 2 147 483 647
		char char2 = 'c';	// pojedyńczy znak Unicode
		long long3 = 1234567890;	// liczby całkowite, zakres od -2^63 do (2^63)-1
		double double4 = 1.12345678; // liczby zmiennoprzecinkowe, ok 15 cyfr po przecinku
		boolean boolean5 = true;	// wartości logiczne, true lub false

		System.out.println(int1);
		System.out.println(char2);
		System.out.println(long3);
		System.out.println(double4);
		System.out.println(boolean5);

	}

}
